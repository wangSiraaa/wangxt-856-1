export function generateId(): string {
  return Date.now().toString(36) + Math.random().toString(36).substr(2);
}

export function createEmptyRecord(operatorName: string): WaterGaugeRecord {
  const now = new Date().toISOString();
  return {
    id: generateId(),
    vesselName: '',
    voyageNo: '',
    operationNo: `OP${Date.now()}`,
    operator: operatorName,
    shipAgent: '',
    operateTime: now,
    frontLeft: null,
    frontRight: null,
    midLeft: null,
    midRight: null,
    aftLeft: null,
    aftRight: null,
    waterDensity: 1.020,
    status: 'draft',
    photos: [],
    createdAt: now,
    updatedAt: now,
  };
}

